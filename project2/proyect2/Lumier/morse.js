function morse (binaryMorseCode){
    //console.log(binaryMorseCode);
    binary = binaryMorseCode.replace(/00000000000000/g,"\n");
    binary = binary.replace(/000000/g," ").replace(/111111/g,"-").replace(/11/g,"·").replace(/00/g,"");
    div = binary.split("\n");
    word = div.map((cv)=>{
        d = cv.split(" ");
        w = d.map((actual)=>{
            return alphabet(actual);
        }).join("");
        return w;
    }).join(" ");
    return word;//Human readable text
}

function alphabet (str){
    switch(str){
        case '·': return 'E';
        case '-': return 'T';
        case '··': return 'I';
        case '·-': return 'A';
        case '-·': return 'N';
        case '--': return 'M';
        case '···': return 'S';
        case '-··': return 'D';
        case '·-·': return 'R';
        case '--·': return 'G';
        case '··-': return 'U';
        case '-·-': return 'K';
        case '·--': return 'W';
        case '---': return 'O';
        case '····': return 'H';
        case '-···': return 'B';
        case '·-··': return 'L';
        case '--··': return 'Z';
        case '··-·': return 'F';
        case '-·-·': return 'C';
        case '·--·': return 'P';
        case '···-': return 'V';
        case '-··-': return 'X';
        case '--·-': return 'Q';
        case '-·--': return 'Y';
        case '·---': return 'J';
    }
}


